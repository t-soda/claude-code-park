use serde::{Deserialize, Serialize};
use ts_rs::TS;

/// How a real hook execution ended. Only set on events backed by an actual
/// execution record in the transcript; reconstructed firings carry None.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize, TS)]
#[ts(export, export_to = "../../src/bindings/")]
pub enum HookOutcome {
    /// The hooks ran to completion (stop_hook_summary without preventedContinuation).
    Completed,
    /// A hook exited 2 and blocked its lifecycle (tool rejected / stop prevented).
    Blocked,
    /// The user interrupted the hook mid-flight (hook_cancelled attachment).
    Cancelled,
}

/// A transient event for a single firing (a reconstructed fact of a lifecycle firing).
/// Generated by the pipeline from the transcript and consumed by the frontend's hookStore.
#[derive(Debug, Clone, Serialize, Deserialize, TS)]
#[ts(export, export_to = "../../src/bindings/")]
pub struct HookEvent {
    pub session_id: String,
    /// Some only for firings inside a sub agent (assigned to an employee sprite).
    pub agent_id: Option<String>,
    /// The reconstructed event name (PreToolUse / PostToolUse / Stop ...).
    pub event: String,
    /// The tool name passed with Pre/PostToolUse, etc.
    pub tool_name: Option<String>,
    /// Firing time (ISO8601 / RFC3339).
    pub ts: String,
    /// The tool_use correlation ID for pairing Pre/PostToolUse.
    /// PreToolUse=tool_use.id / PostToolUse=tool_result.tool_use_id.
    pub correlation_id: Option<String>,
    /// The success/failure of PostToolUse (tool_result.is_error). None otherwise.
    pub is_error: Option<bool>,
    /// How the recorded hook execution ended. None for reconstructed firings
    /// (the transcript records no execution for those).
    pub outcome: Option<HookOutcome>,
    /// Wall time of the recorded hook run (stop_hook_summary / hook_cancelled).
    pub duration_ms: Option<f64>,
    /// The hook command behind the record (first summary command / blocking command).
    pub hook_command: Option<String>,
    /// Why the hook blocked (stopReason / blockingError / PreToolUse stderr).
    pub block_reason: Option<String>,
}
